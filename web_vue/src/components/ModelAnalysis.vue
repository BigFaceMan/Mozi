            trainId: training.id,
        },
        xhrFields: {
            responseType: 'blob'  // хСКшпЙ jQuery ф╗еф║Мш┐ЫхИ╢ Blob чЪДцЦ╣х╝ПцОецФ╢цХ░цНо
        },
        success(resp) {
